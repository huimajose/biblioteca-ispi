import { NextResponse } from "next/server";
import { acceptTransaction } from "@/app/admin/book-requests/server"; // Reusa a função do servidor

export async function POST(req: Request) {
  try {
    const { tid, userId } = await req.json();

    if (!tid || !userId) {
      return NextResponse.json({ error: "Dados inválidos" }, { status: 400 });
    }

    const result = await acceptTransaction(tid, userId);

    if (result.success) {
      return NextResponse.json({ success: true });
    } else {
      return NextResponse.json({ success: false, error: result.error });
    }
  } catch (err: any) {
    console.error("Erro ao aceitar transação:", err);
    return NextResponse.json(
      { success: false, error: "Erro interno do servidor" },
      { status: 500 }
    );
  }
}
